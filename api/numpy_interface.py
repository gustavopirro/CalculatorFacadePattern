import numpy as _np

class MathLibInterface:

    def average(self, numbers_array: list):
        result = _np.average(numbers_array)
        return result
    
    def variation(self, numbers_array: list):
        result = _np.average(numbers_array)
        return result
    
    def standard_deviation(self, numbers_array: list):
        result = _np.average(numbers_array)
        return result